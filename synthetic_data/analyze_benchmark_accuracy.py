"""
Accuracy analyzer for Lead Entry Guard synthetic benchmark.

Runs the pipeline against a JSONL dataset that contains _truth labels
(generated by synthetic_data/generator/build_dataset.py) and produces:

  - Per-bucket confusion matrix
  - False positive / false negative breakdown
  - Overall accuracy metrics
  - Console report + JSON output

NEAR_DUPLICATE bucket is flagged as EXPLORATORY — no hard assertions.

Usage:
    python analyze_benchmark_accuracy.py
    python analyze_benchmark_accuracy.py --dataset synthetic_data/output/messy_leads_10k.jsonl
    python analyze_benchmark_accuracy.py --dataset synthetic_data/output/messy_leads_100k.jsonl
    python analyze_benchmark_accuracy.py --out results/accuracy_report.json
"""
from __future__ import annotations

import argparse
import asyncio
import json
import time
from collections import defaultdict
from pathlib import Path
from typing import Any

import fakeredis.aioredis as fakeredis

from lead_entry_guard.config.tenant import TenantRegistry
from lead_entry_guard.core.models import LeadInput, SourceType
from lead_entry_guard.core.pipeline import IngestionPipeline
from lead_entry_guard.fingerprint.builder import FingerprintBuilder
from lead_entry_guard.lookup.bloom import BloomFilterRegistry
from lead_entry_guard.lookup.duplicate import DuplicateLookupTier
from lead_entry_guard.lookup.redis_store import RedisDuplicateStore, RedisIdempotencyStore
from lead_entry_guard.normalization.normalizer import NormalizationLayer
from lead_entry_guard.policies.engine import PolicyEngine
from lead_entry_guard.security.hmac_keys import HMACKeyManager
from lead_entry_guard.security.vault import InMemoryVaultClient
from lead_entry_guard.telemetry.exporter import TelemetryQueue
from lead_entry_guard.validation.validator import ValidationLayer
from tests.fixtures.common import make_key_ring

# Buckets where _truth is intentionally non-deterministic — reported separately,
# never counted as failures in accuracy metrics.
EXPLORATORY_BUCKETS = {"near_duplicate"}

# Decisions treated as "accepted" for FP/FN classification purposes.
ACCEPTED_DECISIONS = {"PASS", "WARN", "PASS_DEGRADED", "WARN_DEGRADED"}
REJECTED_DECISIONS = {"REJECT"}
DUPLICATE_DECISIONS = {"DUPLICATE_HINT"}

_KEY_RING = {
    "current": {
        "kid": "analyzer-v1",
        "secret_hex": "a" * 64,
        "activated_at": "2026-01-01T00:00:00+00:00",
    }
}


# ── Pipeline factory ──────────────────────────────────────────────────────────

async def build_pipeline() -> IngestionPipeline:
    redis_client = fakeredis.FakeRedis(decode_responses=False)

    km = HMACKeyManager()
    await km.load_from_vault(InMemoryVaultClient(make_key_ring()))

    dup_store = RedisDuplicateStore(redis_client, duplicate_ttl=3600)
    idempotency_store = RedisIdempotencyStore(redis_client, idempotency_ttl=3600)
    bloom_registry = BloomFilterRegistry()
    dup_tier = DuplicateLookupTier(bloom_registry, dup_store)
    fp_builder = FingerprintBuilder(km)

    # NOTE: TelemetryExporter.export_loop() intentionally not started.
    # Queue fills during run — P2_ALERT is expected, not a bug.
    return IngestionPipeline(
        normalizer=NormalizationLayer(),
        validator=ValidationLayer(),
        fingerprint_builder=fp_builder,
        duplicate_tier=dup_tier,
        policy_engine=PolicyEngine(),
        idempotency_store=idempotency_store,
        telemetry_queue=TelemetryQueue(max_size=200_000),
        tenant_registry=TenantRegistry(),
    )


# ── Data structures ───────────────────────────────────────────────────────────

class BucketStats:
    """Per-bucket accuracy counters."""

    def __init__(self, bucket: str, exploratory: bool) -> None:
        self.bucket = bucket
        self.exploratory = exploratory
        self.total = 0
        # confusion matrix: confusion[expected][actual] = count
        self.confusion: dict[str, dict[str, int]] = defaultdict(lambda: defaultdict(int))
        # valid_decisions correctness (actual in valid_decisions)
        self.valid_correct = 0
        self.valid_total = 0
        # strict = single-element valid_decisions
        self.strict_correct = 0
        self.strict_total = 0

    def record(self, expected: str, actual: str, valid_decisions: list[str]) -> None:
        self.total += 1
        self.confusion[expected][actual] += 1
        is_strict = len(valid_decisions) == 1
        correct = actual in valid_decisions
        self.valid_total += 1
        if correct:
            self.valid_correct += 1
        if is_strict:
            self.strict_total += 1
            if correct:
                self.strict_correct += 1

    @property
    def correct(self) -> int:
        return self.valid_correct

    @property
    def accuracy(self) -> float:
        return self.valid_correct / self.valid_total if self.valid_total else 0.0

    @property
    def strict_accuracy(self) -> float:
        return self.strict_correct / self.strict_total if self.strict_total else 0.0

    def false_positives(self) -> list[dict]:
        """
        False positive: expected REJECT/DUPLICATE_HINT but got PASS/WARN.
        Only meaningful for strict buckets (single valid_decision).
        Wide and exploratory buckets return empty — use confusion matrix instead.
        """
        if self.exploratory or self.strict_total != self.total:
            return []
        fps = []
        for expected, actuals in self.confusion.items():
            if expected in REJECTED_DECISIONS | DUPLICATE_DECISIONS:
                for actual, count in actuals.items():
                    if actual in ACCEPTED_DECISIONS and count > 0:
                        fps.append({"expected": expected, "actual": actual, "count": count})
        return fps

    def false_negatives(self) -> list[dict]:
        """
        False negative: expected PASS/WARN but got REJECT/DUPLICATE_HINT.
        Only meaningful for strict buckets (single valid_decision).
        Wide and exploratory buckets return empty — use confusion matrix instead.
        """
        if self.exploratory or self.strict_total != self.total:
            return []
        fns = []
        for expected, actuals in self.confusion.items():
            if expected in ACCEPTED_DECISIONS:
                for actual, count in actuals.items():
                    if actual in REJECTED_DECISIONS | DUPLICATE_DECISIONS and count > 0:
                        fns.append({"expected": expected, "actual": actual, "count": count})
        return fns

    def to_dict(self) -> dict[str, Any]:
        matrix = {e: dict(actuals) for e, actuals in self.confusion.items()}
        fps = self.false_positives()
        fns = self.false_negatives()
        fp_total = sum(x["count"] for x in fps)
        fn_total = sum(x["count"] for x in fns)
        return {
            "bucket": self.bucket,
            "exploratory": self.exploratory,
            "total": self.total,
            "correct": self.valid_correct,
            "accuracy": round(self.accuracy, 4),
            "strict_correct": self.strict_correct,
            "strict_total": self.strict_total,
            "strict_accuracy": round(self.strict_accuracy, 4) if self.strict_total else None,
            "false_positives": fps,
            "false_positives_total": fp_total,
            "false_negatives": fns,
            "false_negatives_total": fn_total,
            "confusion_matrix": matrix,
        }


# ── Runner ────────────────────────────────────────────────────────────────────

async def run_analysis(dataset_path: Path) -> dict[str, Any]:
    pipeline = await build_pipeline()

    bucket_stats: dict[str, BucketStats] = {}
    total_records = 0
    records_without_truth = 0

    print(f"\n[analyze] Loading {dataset_path} ...")
    start = time.monotonic()

    with dataset_path.open("r", encoding="utf-8") as f:
        records = [json.loads(line) for line in f if line.strip()]

    print(f"  Loaded {len(records):,} records")
    print(f"  Running pipeline ...")

    for i, record in enumerate(records):
        truth = record.get("_truth")
        if not truth:
            records_without_truth += 1
            continue

        bucket = truth.get("variant_type", "unknown")
        expected = truth.get("expected_decision", "UNKNOWN")
        exploratory = bucket in EXPLORATORY_BUCKETS

        if bucket not in bucket_stats:
            bucket_stats[bucket] = BucketStats(bucket, exploratory)

        source_type_raw = record.get("source_type", "API")
        try:
            source_type = SourceType(source_type_raw)
        except ValueError:
            source_type = SourceType.API

        lead = LeadInput(
            tenant_id=record["tenant_id"],
            source_id=record.get("source_id"),
            source_type=source_type,
            email=record.get("email"),
            phone=record.get("phone"),
            first_name=record.get("first_name"),
            last_name=record.get("last_name"),
            company=record.get("company"),
            extra=record.get("extra", {}),
        )

        result = await pipeline.process(lead)
        actual = result.decision.value

        valid_decisions = truth.get("valid_decisions", [truth.get("expected_decision", "UNKNOWN")])
        bucket_stats[bucket].record(expected, actual, valid_decisions)
        total_records += 1

        if (i + 1) % 10_000 == 0:
            elapsed = time.monotonic() - start
            print(f"  {i+1:>7,} / {len(records):,}  ({elapsed:.1f}s)")

    duration = time.monotonic() - start
    print(f"\n  Done — {total_records:,} records in {duration:.1f}s "
          f"({total_records / duration:.0f} records/s)")

    # ── Assemble report ───────────────────────────────────────────────────────

    all_correct = sum(s.valid_correct for s in bucket_stats.values() if not s.exploratory)
    all_total   = sum(s.valid_total   for s in bucket_stats.values() if not s.exploratory)
    overall_accuracy = all_correct / all_total if all_total else 0.0

    strict_correct = sum(s.strict_correct for s in bucket_stats.values() if not s.exploratory)
    strict_total   = sum(s.strict_total   for s in bucket_stats.values() if not s.exploratory)
    strict_accuracy = strict_correct / strict_total if strict_total else 0.0

    fp_total = sum(
        sum(x["count"] for x in s.false_positives())
        for s in bucket_stats.values() if not s.exploratory
    )
    fn_total = sum(
        sum(x["count"] for x in s.false_negatives())
        for s in bucket_stats.values() if not s.exploratory
    )

    report = {
        "dataset": str(dataset_path),
        "total_records": total_records,
        "records_without_truth": records_without_truth,
        "duration_s": round(duration, 2),
        "overall": {
            "accuracy": round(overall_accuracy, 4),
            "correct": all_correct,
            "total_non_exploratory": all_total,
            "strict_accuracy": round(strict_accuracy, 4),
            "strict_correct": strict_correct,
            "strict_total": strict_total,
            "false_positives_total": fp_total,
            "false_negatives_total": fn_total,
            "note": (
                "accuracy = actual in valid_decisions. "
                "strict_accuracy = single-outcome buckets only (clean, broken, exact_duplicate). "
                "Exploratory buckets excluded from both."
            ),
        },
        "buckets": {
            name: stats.to_dict()
            for name, stats in sorted(bucket_stats.items())
        },
    }

    return report


# ── Console printer ───────────────────────────────────────────────────────────

def print_report(report: dict[str, Any]) -> None:
    W = 72
    print("\n" + "═" * W)
    print("  ACCURACY REPORT — Lead Entry Guard Synthetic Benchmark")
    print("═" * W)
    print(f"  Dataset : {report['dataset']}")
    print(f"  Records : {report['total_records']:,}")
    print(f"  Duration: {report['duration_s']}s")

    ov = report["overall"]
    print(f"\n  Overall accuracy  (non-exploratory, valid_decisions): "
          f"{ov['accuracy']*100:.2f}%  "
          f"({ov['correct']:,} / {ov['total_non_exploratory']:,})")
    print(f"  Strict accuracy   (single-outcome buckets only):       "
          f"{ov['strict_accuracy']*100:.2f}%  "
          f"({ov['strict_correct']:,} / {ov['strict_total']:,})")
    print(f"  Strict false positives (should stop, didn't — strict buckets only): {ov['false_positives_total']:,}")
    print(f"  Strict false negatives (valid lead blocked  — strict buckets only): {ov['false_negatives_total']:,}")

    print("\n" + "─" * W)
    print(f"  {'BUCKET':<22} {'TOTAL':>7}  {'CORRECT':>7}  {'ACCURACY':>8}  "
          f"{'sFP':>5}  {'sFN':>5}  {'NOTE'}")
    print("─" * W)

    for name, b in report["buckets"].items():
        if b["exploratory"]:
            note = "⚠  EXPLORATORY"
            acc_str = "  n/a "
        elif b["strict_total"] == b["total"]:
            note = "strict"
            acc_str = f"{b['accuracy']*100:.1f}%"
        else:
            note = "wide  (sFP/sFN n/a)"
            acc_str = f"{b['accuracy']*100:.1f}%"
        print(f"  {name:<22} {b['total']:>7,}  {b['correct']:>7,}  {acc_str:>8}  "
              f"{b['false_positives_total']:>5}  {b['false_negatives_total']:>5}  {note}")

    print("─" * W)

    print("\n  CONFUSION MATRICES  (rows = expected, cols = actual)\n")
    for name, b in report["buckets"].items():
        if b["exploratory"]:
            tag = "  [EXPLORATORY — no hard assertion]"
        elif b["strict_total"] == b["total"]:
            tag = "  [strict — single valid outcome]"
        else:
            tag = "  [wide — multiple valid outcomes]"
        print(f"  ┌─ {name}{tag}")
        matrix = b["confusion_matrix"]
        all_actuals = sorted({a for row in matrix.values() for a in row})
        header = "  │  " + f"{'expected':<20}" + "".join(f"{a:>16}" for a in all_actuals)
        print(header)
        for expected, actuals in sorted(matrix.items()):
            row = "  │  " + f"{expected:<20}" + "".join(
                f"{actuals.get(a, 0):>16,}" for a in all_actuals
            )
            print(row)
        fps = b["false_positives"]
        fns = b["false_negatives"]
        if fps:
            fp_str = ", ".join(f"{x['expected']}→{x['actual']}:{x['count']}" for x in fps)
            print(f"  │  FP: {fp_str}")
        if fns:
            fn_str = ", ".join(f"{x['expected']}→{x['actual']}:{x['count']}" for x in fns)
            print(f"  │  FN: {fn_str}")
        print()

    print("═" * W)
    print("  strict  = single valid outcome, hard assertion")
    print("  wide    = multiple valid outcomes (mutation non-determinism)")
    print("  EXPLORATORY = no assertion, reported for sizing only")
    print("  Next step: split wide buckets into typed subtypes (dirty_valid,")
    print("  dirty_invalid_phone, ...) once NormalizationLayer is imported")
    print("  into the generator.")
    print("═" * W + "\n")


# ── CLI ───────────────────────────────────────────────────────────────────────

def main() -> None:
    parser = argparse.ArgumentParser(
        description="Accuracy analyzer — compare _truth labels vs pipeline decisions"
    )
    parser.add_argument(
        "--dataset",
        type=Path,
        default=Path("synthetic_data/output/messy_leads_100k.jsonl"),
        help="Path to JSONL dataset with _truth labels",
    )
    parser.add_argument(
        "--out",
        type=Path,
        default=Path("synthetic_data/output/accuracy_report.json"),
        help="Output path for JSON report",
    )
    args = parser.parse_args()

    if not args.dataset.exists():
        raise SystemExit(f"Dataset not found: {args.dataset}")

    report = asyncio.run(run_analysis(args.dataset))

    print_report(report)

    args.out.parent.mkdir(parents=True, exist_ok=True)
    with args.out.open("w", encoding="utf-8") as f:
        json.dump(report, f, indent=2, ensure_ascii=False)
    print(f"  JSON report → {args.out}\n")


if __name__ == "__main__":
    main()
